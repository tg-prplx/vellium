export type TranslationKey = keyof typeof translations.en;
type TranslationDict = Record<TranslationKey, string>;

const completedTranslations: Record<Locale, TranslationDict> = {
  en: translations.en as TranslationDict,
  ru: translations.ru as TranslationDict,
  zh: { ...(translations.en as TranslationDict), ...(translations.zh as Partial<TranslationDict>) } as TranslationDict,
  ja: { ...(translations.en as TranslationDict), ...(translations.ja as Partial<TranslationDict>) } as TranslationDict
};

export const I18nContext = createContext<Locale>("en");

export function useI18n() {
  const locale = useContext(I18nContext);
  function t(key: TranslationKey): string {
    return completedTranslations[locale][key] ?? translations.en[key] ?? key;
  }
  return { t, locale };
}
